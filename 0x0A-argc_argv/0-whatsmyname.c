#include <stdio.h>
/**
* main - Entry point. Prints the name of the program.
* @argc: Number of command-line arguments.
* @argv: Array of pointers to strings representing the arguments.
* Return: Always 0.
*/
int main(int __attribute__((unused)) argc, char *argv[])
{
printf("%s\n", argv[0]);
return 0;
}
